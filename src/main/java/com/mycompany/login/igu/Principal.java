    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        String usuario = txtUsuario.getText();
        String contrasenia = txtContrasenia.getText();
        Usuario usr = conLogico.validarUsuario(usuario, contrasenia);

        if (usr != null) {
            String rol = usr.getUnRol().getNombreRol();
            if (rol.equals("admin")) {
                PrincipalAdmin pantaAdmin = new PrincipalAdmin(conLogico, usr);
                pantaAdmin.setVisible(true);
                pantaAdmin.setLocationRelativeTo(null);
                this.dispose();
            } 
            if (rol.equalsIgnoreCase("user")) {
                PrincipalUser pantaUser = new PrincipalUser(conLogico, usr);
                pantaUser.setVisible(true);
                pantaUser.setLocationRelativeTo(null);
                this.dispose();
            }
        } else {
            mostrarMensaje("La contrasenia o el usuario Ingresados son incorrectos","ERROR","Usuario o Contrasenia Equivocada");
            txtMensaje.setText("La Contrasenia o el Usuario son Incorrectos");
            
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

